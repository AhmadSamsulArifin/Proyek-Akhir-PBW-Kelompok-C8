<script>

document.addEventListener('DOMContentLoaded', () => {
    const obs = new IntersectionObserver(entries => {
        entries.forEach(e => { if (e.isIntersecting) { e.target.classList.add('mk-revealed'); obs.unobserve(e.target); } });
    }, { threshold: 0.12 });
    document.querySelectorAll('[data-aos]').forEach(el => obs.observe(el));


    setInterval(() => sliderNav(1), 4000);
});


let sliderIdx = 0;

function sliderNav(dir) {
    const slides = document.querySelectorAll('.mk-slide');
    if (!slides.length) return;

    sliderIdx = (sliderIdx + dir + slides.length) % slides.length;
    sliderGoTo(sliderIdx);
}

function sliderGoTo(i) {
    const slides = document.querySelectorAll('.mk-slide');
    if (!slides.length) return;

    sliderIdx = i;
    const track = document.getElementById('mkSliderTrack');

    if (track) {
        track.style.transform = `translateX(-${i * 100}%)`;
    }

    document.querySelectorAll('.mk-dot').forEach((d, idx) => {
        d.classList.toggle('active', idx === i);
    });
}

let lbIdx = 0;
let lbSlides = [];
function openLightbox(el) {
    lbSlides = [...document.querySelectorAll('.mk-slide')];
    lbIdx = lbSlides.indexOf(el);
    _lbShow(lbIdx);
    document.getElementById('mkLightbox').classList.add('active');
    document.body.style.overflow = 'hidden';
}
function _lbShow(i) {
    const el = lbSlides[i];
    document.getElementById('mkLightboxImg').src = el.dataset.src;
    document.getElementById('mkLightboxCaption').textContent = el.dataset.caption;
}
function lbNav(dir) {
    lbIdx = (lbIdx + dir + lbSlides.length) % lbSlides.length;
    _lbShow(lbIdx);
}
function closeLightbox() {
    document.getElementById('mkLightbox').classList.remove('active');
    document.body.style.overflow = '';
}
document.addEventListener('keydown', e => {
    if (e.key === 'Escape') closeLightbox();
    if (e.key === 'ArrowRight') lbNav(1);
    if (e.key === 'ArrowLeft')  lbNav(-1);
});
</script>
